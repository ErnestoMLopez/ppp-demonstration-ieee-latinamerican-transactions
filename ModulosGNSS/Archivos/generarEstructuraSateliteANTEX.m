...% Parámetros de PCV no-azimutal
...% 1er indice: Frecuencia, 3er indice: GNSS, 4to indice: Ángulo zenit/nadir
	'DZEN',			0, ...
	'ZEN1',			0, ...
	'ZEN2',			0, ...
	'NZEN',			0, ...
	'PCVZEN',		zeros(0,0,1) ...
	);

end